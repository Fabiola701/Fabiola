cuaca = input("Cuacanya gimana?")

if cuaca == "hujan":
    print("Jangan lupa payung")
elif cuaca == "panas":
    print("Pakai sunscreen")
elif cuaca == "berawan":
    print("Mendung tapi belum tentu hujan")
elif cuaca == "berangin":
    print("Pakai jaket biar tidak masuk angin")
else:
    print("Cuaca tidak bisa ditebak")
